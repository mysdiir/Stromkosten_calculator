package model;

public class Kosten {
    private float kostenTag;
    private float kostenWoche;
    private float kostenMonat;
    private float kostenJahr;

    public Kosten() {

    }

    public float getKostenTag() {
        return kostenTag;
    }
    public void setKostenTag(float kostenTag) {
        this.kostenTag = kostenTag;
    }
    public float getKostenWoche() {
        return kostenWoche;
    }
    public void setKostenWoche(float kostenWoche) {
        this.kostenWoche = kostenWoche;
    }
    public float getKostenMonat() {
        return kostenMonat;
    }
    public void setKostenMonat(float kostenMonat) {
        this.kostenMonat = kostenMonat;
    }
    public float getKostenJahr() {
        return kostenJahr;
    }
    public void setKostenJahr(float kostenJahr) {
        this.kostenJahr = kostenJahr;
    }
}
