package model;

public class Verbrauch {
    private float verbrauchTag;
    private float verbrauchWoche;
    private float verbrauchMonat;
    private float verbrauchJahr;

    public Verbrauch() {
    }

    public float getVerbrauchTag() {
        return verbrauchTag;
    }

    public void setVerbrauchTag(float verbrauchTag) {
        this.verbrauchTag = verbrauchTag;
    }

    public float getVerbrauchWoche() {
        return verbrauchWoche;
    }

    public void setVerbrauchWoche(float verbrauchWoche) {
        this.verbrauchWoche = verbrauchWoche;
    }

    public float getVerbrauchMonat() {
        return verbrauchMonat;
    }

    public void setVerbrauchMonat(float verbrauchMonat) {
        this.verbrauchMonat = verbrauchMonat;
    }

    public float getVerbrauchJahr() {
        return verbrauchJahr;
    }

    public void setVerbrauchJahr(float verbrauchJahr) {
        this.verbrauchJahr = verbrauchJahr;
    }


}
